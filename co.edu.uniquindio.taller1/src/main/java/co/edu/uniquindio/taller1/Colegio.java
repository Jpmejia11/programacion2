package co.edu.uniquindio.taller1;

public class Colegio {

    private String grado;
    private int puestos;

    public Colegio(String grado, int puestos) {
        this.grado = grado;
        this.puestos = puestos;
    }

    public String getGrado() {
        return grado;
    }

    public int getPuestos() {
        return puestos;
    }
}
